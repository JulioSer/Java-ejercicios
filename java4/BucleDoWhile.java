public class BucleDoWhile {
    public static void main(String[] args) {
        int numeroDoWhile = 0;
        do{
        numeroDoWhile++;
        System.out.println("Valor adentro: " + numeroDoWhile);
        }while (numeroDoWhile > 3);
        System.out.println("Valor afuera: " + numeroDoWhile);
    }
}
